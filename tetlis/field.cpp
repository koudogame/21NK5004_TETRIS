#include "field.h"
#include "Common.h"


//*****************************************************************************
//  コンストラクタ
//*****************************************************************************
Field::Field() : texture_( -1 )
{
}


//*****************************************************************************
//  デストラクタ
//*****************************************************************************
Field::~Field()
{
    destroy();
}


//*****************************************************************************
//  初期化
//*****************************************************************************
bool Field::init()
{
    // テクスチャの読み込み
    texture_ = LoadGraph( "Tetris11_SingleT.png" );
    if( texture_ == -1 )
    {
        // エラー
        return false;
    }

    return true;
}


//*****************************************************************************
//  更新
//*****************************************************************************
bool Field::update()
{
    return true;
}


//*****************************************************************************
//  描画
//*****************************************************************************
void Field::draw()
{
    // 背景描画
    DrawRectGraph( 0, 0, 0, 0, kWindowWidth, kWindowHeight, texture_, false );
}



//*****************************************************************************
//  破棄
//*****************************************************************************
void Field::destroy()
{
    // グラフィックハンドルの解放
    SAFE_DELETE_GRAPH( texture_ );
}